//Variables
// Syntax
// Var - Variable type variable name = values
// var - Redeclare
// a - Reassign
var a = 10;
var a = 20;
var a = 30;
console.log(a);

//Let
// let - Can't Redeclare for same variable name assigned
// b/f - Can be reassigned with values
let b = 40;
    b = 60;
let f = 50;
    f = 100;
console.log(b);
console.log(f);


//CONST
// const - Can't Redeclare for same variable name assigned
// c/d - Can't reassigned with values
const c = 60
const d = 70
console.log(c);
console.log(d);

